//   = assignmentt operator
//  == comparison operator
// === strict equality operator
//  != inequality operator
// !== strict inequality operator

const PI = 3.14;
if (PI === "3.14"){                 // Compare value and data type
    console.log("That is PI");
}
else{
    console.log("That is not PI");
}

if (PI !== "3.14"){                 // Compare value and data type
    console.log("That is not PI");
}
else{
    console.log("That is PI");
}